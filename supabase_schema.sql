-- 1. Create Books Table
create table if not exists public.books (
  id bigint generated by default as identity primary key,
  title text not null,
  author text not null,
  category text not null,
  description text not null,
  price numeric not null,
  stock bigint not null default 0,
  published_year bigint not null,
  cover_gradient text not null,
  rating numeric not null default 4.0
);

-- 2. Create User Profiles Table (role management)
create table if not exists public.profiles (
  id uuid primary key references auth.users on delete cascade,
  role text not null default 'customer' check (role in ('customer', 'admin'))
);

-- 3. Create Orders Table
create table if not exists public.orders (
  id uuid default gen_random_uuid() primary key,
  created_at timestamptz default now() not null,
  customer_name text not null,
  customer_email text not null,
  customer_phone text not null,
  delivery_address text not null,
  order_notes text,
  payment_method text not null,
  payment_details jsonb not null default '{}'::jsonb,
  total_amount numeric not null,
  items jsonb not null default '[]'::jsonb
);

-- 4. Enable Row Level Security (RLS) bypass for quick development,
-- or configure public access. For client-side quick integrations, 
-- we will disable RLS on public tables so client keys can read/write.
alter table public.books disable row level security;
alter table public.profiles disable row level security;
alter table public.orders disable row level security;

