-- 6. Setup Auth Profile Trigger
-- Automatically create a profile entry when a user registers via Supabase Auth
create or replace function public.handle_new_user()
returns trigger as $$
begin
  -- The first user to sign up will be the admin (optional helper), 
  -- otherwise all new signups default to 'customer'.
  if not exists (select 1 from public.profiles) then
    insert into public.profiles (id, role)
    values (new.id, 'admin');
  else
    insert into public.profiles (id, role)
    values (new.id, 'customer');
  end if;
  return new;
end;
$$ language plpgsql security definer;

-- Trigger definition
drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created
  after insert on auth.users
  for each row execute procedure public.handle_new_user();
